#Realice un programa que lea dos números y diga cuál es el mayor.


numero1 = float(input("Ingrese la primera cantidad: "))
numero2 = float(input("Ingrese la segunda cantidad: "))

if numero1 > numero2:
    print ("El numero 1 es mayuor que el numero 2")
else:
    print("El numero 2 es mayor que el numero 1")